package com.craneos.java11.part1.chapter1.examples;

public class DefaultValues {

    static boolean btype;
    // for NON decimal values
    static byte bbyte;
    static short bshort;
    static int bint;
    static int blong;
    static int bfloat;
    static int bdouble;
    static char bchar;

    public static void main(String[] args) {
        System.out.println("btype="+btype);
        System.out.println("bbyte="+bbyte);
        System.out.println("bshort="+bshort);
        System.out.println("bint="+bint);
        System.out.println("blong="+blong);
        System.out.println("bfloat="+bfloat);
        System.out.println("bdouble="+bdouble);
        System.out.println("bchar="+bchar);
    }

}
